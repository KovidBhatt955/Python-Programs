# --- String Operation Program ---

# 1. Define a string variable
original_message = "Congratulations on your achievement!"

# 2. Convert to uppercase
upper_msg = original_message.upper()

# 3. Find the length of the string
length_of_msg = len(original_message)

# 4. Replace a word within the string
new_message = original_message.replace("achievement", "success")

# 5. Print results to demonstrate operations
print("Original:", original_message)
print("Uppercase:", upper_msg)
print("Length:", length_of_msg)
print("Replaced:", new_message)
