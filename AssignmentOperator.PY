# 1. Simple Assignment (=)
# Assigns the value on the right to the variable on the left
x = 10
print(f"Simple Assignment (x = 10): {x}")

# 2. Addition Assignment (+=)
# Equivalent to: x = x + 5
x += 5
print(f"Addition Assignment (x = 5): {x}")
# 3. Subtraction Assignment
# Equivalent to: x = x - 3
x -= 3
print(f"Subtraction Assignment (x -= 3): {x}")

# 4. Multiplication Assignment (*=)
# Equivalent to: x = x * 2
x *= 2
print(f"Multiplication Assignment (x *= 2): {x}")

# 5. Division Assignment
# Equivalent to: x = x / 4
x = 4
print(f"Division Assignment (x /= 4): {x}")

# 6. Modulus Assignment (%=)
# Equivalent to: x = x % 2
print(f"Modulus Assignment (x %= 2): {x}")

#7. Floor Division Assignment (//=)
# Equivalent to: y = y // 3 (divides and rounds down to nearest integer)
y = 10
y //=3
print(f"Floor Division Assignment (10 //= 3): {x}")

# 8. Exponent Assignment (**=)
# Equivalent to: z = z ** 3 (z cubed)
z = 2
z **= 3
print(f"Exponent Assignment (2 **= 3): {x}")
